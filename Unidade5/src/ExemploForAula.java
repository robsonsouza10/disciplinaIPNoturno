public class ExemploForAula {
    public static void main(String[] args) {
        //ordem crescente
        
        for(int contador = 1; contador <= 5; contador++){
            System.out.println(contador);
        }

        System.out.println("-----------------");
        //ordem decrescente
        for(int contador = 5; contador >= 1; contador--){
            System.out.println(contador);
        }

        
    }
}
